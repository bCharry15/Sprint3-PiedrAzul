/* Justificacion:
   Este indice mejora consultas frecuentes de agenda donde se filtran citas
   por fecha y estado, por ejemplo: citas programadas de un dia especifico. */
CREATE INDEX IDX_CITA_FECHA_ESTADO
ON CITA(FECHA_CITA, ESTADO);

/* Justificacion:
   Este indice mejora busquedas y ordenamientos de pacientes por apellidos
   y nombres, caso comun en admision/agendamiento. */
CREATE INDEX IDX_PACIENTE_APELLIDOS_NOMBRES
ON PACIENTE(APELLIDOS, NOMBRES);

