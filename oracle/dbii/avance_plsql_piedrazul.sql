CREATE OR REPLACE PACKAGE BODY PKG_GESTION_PACIENTES AS

    E_PACIENTE_NO_EXISTE EXCEPTION;
    E_EMAIL_INVALIDO     EXCEPTION;

    PROCEDURE INSERTAR_PACIENTE(
        P_TIPO_DOCUMENTO   IN PACIENTE.TIPO_DOCUMENTO%TYPE,
        P_NUM_DOCUMENTO    IN PACIENTE.NUM_DOCUMENTO%TYPE,
        P_NOMBRES          IN PACIENTE.NOMBRES%TYPE,
        P_APELLIDOS        IN PACIENTE.APELLIDOS%TYPE,
        P_FECHA_NACIMIENTO IN PACIENTE.FECHA_NACIMIENTO%TYPE,
        P_CELULAR          IN PACIENTE.CELULAR%TYPE,
        P_DIRECCION        IN PACIENTE.DIRECCION%TYPE,
        P_EMAIL            IN PACIENTE.EMAIL%TYPE,
        P_ID_GENERADO      OUT PACIENTE.ID_PACIENTE%TYPE
    ) IS
    BEGIN
        IF P_EMAIL IS NOT NULL AND NOT REGEXP_LIKE(P_EMAIL, '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$') THEN
            RAISE E_EMAIL_INVALIDO;
        END IF;

        P_ID_GENERADO := SEQ_PACIENTE.NEXTVAL;

        INSERT INTO PACIENTE(
            ID_PACIENTE, TIPO_DOCUMENTO, NUM_DOCUMENTO, NOMBRES, APELLIDOS,
            FECHA_NACIMIENTO, CELULAR, DIRECCION, EMAIL, ESTADO
        ) VALUES (
            P_ID_GENERADO, UPPER(P_TIPO_DOCUMENTO), P_NUM_DOCUMENTO,
            INITCAP(P_NOMBRES), INITCAP(P_APELLIDOS), P_FECHA_NACIMIENTO,
            P_CELULAR, P_DIRECCION, LOWER(P_EMAIL), 'A'
        );

        DBMS_OUTPUT.PUT_LINE('Paciente insertado con ID: ' || P_ID_GENERADO);

    EXCEPTION
        WHEN E_EMAIL_INVALIDO THEN
            RAISE_APPLICATION_ERROR(-20001, 'El correo del paciente no tiene un formato valido.');
        WHEN DUP_VAL_ON_INDEX THEN
            RAISE_APPLICATION_ERROR(-20002, 'Ya existe un paciente con ese numero de documento.');
        WHEN VALUE_ERROR THEN
            RAISE_APPLICATION_ERROR(-20003, 'Error de tipo o longitud en los datos del paciente.');
        WHEN OTHERS THEN
            RAISE_APPLICATION_ERROR(-20004, 'Error inesperado al insertar paciente: ' || SQLERRM);
    END INSERTAR_PACIENTE;

    PROCEDURE ACTUALIZAR_PACIENTE(
        P_ID_PACIENTE      IN PACIENTE.ID_PACIENTE%TYPE,
        P_CELULAR          IN PACIENTE.CELULAR%TYPE,
        P_DIRECCION        IN PACIENTE.DIRECCION%TYPE,
        P_EMAIL            IN PACIENTE.EMAIL%TYPE
    ) IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT COUNT(*) INTO V_TOTAL
        FROM PACIENTE
        WHERE ID_PACIENTE = P_ID_PACIENTE;

        IF V_TOTAL = 0 THEN
            RAISE E_PACIENTE_NO_EXISTE;
        END IF;

        IF P_EMAIL IS NOT NULL AND NOT REGEXP_LIKE(P_EMAIL, '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$') THEN
            RAISE E_EMAIL_INVALIDO;
        END IF;

        UPDATE PACIENTE
        SET CELULAR = P_CELULAR,
            DIRECCION = P_DIRECCION,
            EMAIL = LOWER(P_EMAIL)
        WHERE ID_PACIENTE = P_ID_PACIENTE;

        DBMS_OUTPUT.PUT_LINE('Paciente actualizado: ' || P_ID_PACIENTE);

    EXCEPTION
        WHEN E_PACIENTE_NO_EXISTE THEN
            RAISE_APPLICATION_ERROR(-20005, 'No existe el paciente que se desea actualizar.');
        WHEN E_EMAIL_INVALIDO THEN
            RAISE_APPLICATION_ERROR(-20006, 'El correo actualizado no tiene un formato valido.');
        WHEN OTHERS THEN
            RAISE_APPLICATION_ERROR(-20007, 'Error inesperado al actualizar paciente: ' || SQLERRM);
    END ACTUALIZAR_PACIENTE;

    PROCEDURE CONSULTAR_PACIENTE(
        P_ID_PACIENTE IN PACIENTE.ID_PACIENTE%TYPE,
        P_PACIENTE    OUT T_PACIENTE_REC
    ) IS
    BEGIN
        SELECT ID_PACIENTE, TIPO_DOCUMENTO, NUM_DOCUMENTO, NOMBRES, APELLIDOS,
               FECHA_NACIMIENTO, CELULAR, DIRECCION, EMAIL, ESTADO
        INTO P_PACIENTE
        FROM PACIENTE
        WHERE ID_PACIENTE = P_ID_PACIENTE;

    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RAISE_APPLICATION_ERROR(-20008, 'No existe el paciente consultado.');
        WHEN TOO_MANY_ROWS THEN
            RAISE_APPLICATION_ERROR(-20009, 'La consulta retorno mas de un paciente.');
        WHEN OTHERS THEN
            RAISE_APPLICATION_ERROR(-20010, 'Error inesperado al consultar paciente: ' || SQLERRM);
    END CONSULTAR_PACIENTE;

    PROCEDURE ELIMINAR_PACIENTE(
        P_ID_PACIENTE IN PACIENTE.ID_PACIENTE%TYPE
    ) IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT COUNT(*) INTO V_TOTAL
        FROM PACIENTE
        WHERE ID_PACIENTE = P_ID_PACIENTE;

        IF V_TOTAL = 0 THEN
            RAISE E_PACIENTE_NO_EXISTE;
        END IF;

        /* Eliminacion logica para no romper citas historicas. */
        UPDATE PACIENTE
        SET ESTADO = 'I'
        WHERE ID_PACIENTE = P_ID_PACIENTE;

        DBMS_OUTPUT.PUT_LINE('Paciente inactivado: ' || P_ID_PACIENTE);

    EXCEPTION
        WHEN E_PACIENTE_NO_EXISTE THEN
            RAISE_APPLICATION_ERROR(-20011, 'No existe el paciente que se desea eliminar/inactivar.');
        WHEN OTHERS THEN
            RAISE_APPLICATION_ERROR(-20012, 'Error inesperado al eliminar paciente: ' || SQLERRM);
    END ELIMINAR_PACIENTE;

    FUNCTION EXISTE_PACIENTE(
        P_NUM_DOCUMENTO IN PACIENTE.NUM_DOCUMENTO%TYPE
    ) RETURN BOOLEAN IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT COUNT(*) INTO V_TOTAL
        FROM PACIENTE
        WHERE NUM_DOCUMENTO = P_NUM_DOCUMENTO;

        RETURN V_TOTAL > 0;
    EXCEPTION
        WHEN OTHERS THEN
            RETURN FALSE;
    END EXISTE_PACIENTE;

END PKG_GESTION_PACIENTES;
/

