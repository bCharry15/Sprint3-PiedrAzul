        INTO V_TOTAL
        FROM CITA
        WHERE ID_CITA = P_ID_CITA;

        IF V_TOTAL = 0 THEN
            RAISE E_CITA_NO_EXISTE;
        END IF;

        VALIDAR_ESTADO(P_ESTADO);

        UPDATE CITA
        SET ESTADO = UPPER(P_ESTADO)
        WHERE ID_CITA = P_ID_CITA;

        DBMS_OUTPUT.PUT_LINE('Estado actualizado para la cita: ' || P_ID_CITA);

    EXCEPTION
        WHEN E_CITA_NO_EXISTE THEN
