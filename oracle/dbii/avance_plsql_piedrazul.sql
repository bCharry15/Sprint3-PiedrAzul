/* ----------------------------------------------------------------------------
   REPORTE 1: Citas por profesional usando cursor explicito, FOR y CASE.
   Cursor 1.
---------------------------------------------------------------------------- */
DECLARE
    CURSOR C_CITAS_PROFESIONAL IS
        SELECT M.ID_MEDICO,
               M.NOMBRES || ' ' || M.APELLIDOS AS PROFESIONAL,
               COUNT(C.ID_CITA) AS TOTAL_CITAS
        FROM MEDICO_TERAPISTA M
        LEFT JOIN CITA C ON C.ID_MEDICO = M.ID_MEDICO
        GROUP BY M.ID_MEDICO, M.NOMBRES, M.APELLIDOS
        ORDER BY TOTAL_CITAS DESC;

    V_CLASIFICACION VARCHAR2(30);
BEGIN
    DBMS_OUTPUT.PUT_LINE('REPORTE 1: CANTIDAD DE CITAS POR PROFESIONAL');

    FOR R IN C_CITAS_PROFESIONAL LOOP
        CASE
            WHEN R.TOTAL_CITAS = 0 THEN V_CLASIFICACION := 'SIN CITAS';
            WHEN R.TOTAL_CITAS BETWEEN 1 AND 2 THEN V_CLASIFICACION := 'CARGA BAJA';
            ELSE V_CLASIFICACION := 'CARGA ALTA';
        END CASE;

        DBMS_OUTPUT.PUT_LINE(R.PROFESIONAL || ' -> ' || R.TOTAL_CITAS || ' citas - ' || V_CLASIFICACION);
    END LOOP;
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Error en reporte 1: ' || SQLERRM);
END;
/

/* ----------------------------------------------------------------------------
   REPORTE 2: Citas de un paciente usando cursor con parametro e IF.
   Cursor 2 parametrizado.
---------------------------------------------------------------------------- */
DECLARE
    CURSOR C_CITAS_PACIENTE(P_ID_PACIENTE NUMBER) IS
        SELECT C.ID_CITA, C.FECHA_CITA, C.HORA_CITA, C.ESTADO,
               M.NOMBRES || ' ' || M.APELLIDOS AS PROFESIONAL
        FROM CITA C
        INNER JOIN MEDICO_TERAPISTA M ON M.ID_MEDICO = C.ID_MEDICO
        WHERE C.ID_PACIENTE = P_ID_PACIENTE
        ORDER BY C.FECHA_CITA, C.HORA_CITA;

    V_ID_PACIENTE PACIENTE.ID_PACIENTE%TYPE := 1;
    V_TOTAL NUMBER := 0;
BEGIN
    DBMS_OUTPUT.PUT_LINE('REPORTE 2: CITAS DEL PACIENTE ID ' || V_ID_PACIENTE);

    FOR R IN C_CITAS_PACIENTE(V_ID_PACIENTE) LOOP
        V_TOTAL := V_TOTAL + 1;
        DBMS_OUTPUT.PUT_LINE('Cita ' || R.ID_CITA || ' - ' || TO_CHAR(R.FECHA_CITA,'YYYY-MM-DD') ||
                             ' ' || R.HORA_CITA || ' - ' || R.ESTADO || ' - ' || R.PROFESIONAL);
    END LOOP;

    IF V_TOTAL = 0 THEN
        DBMS_OUTPUT.PUT_LINE('El paciente no tiene citas registradas.');
    ELSE
        DBMS_OUTPUT.PUT_LINE('Total citas encontradas: ' || V_TOTAL);
    END IF;
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Error en reporte 2: ' || SQLERRM);
END;
/

/* ----------------------------------------------------------------------------
   REPORTE 3: Auditoria usando cursor explicito, WHILE y FETCH.
   Cursor 3.
---------------------------------------------------------------------------- */
DECLARE
    CURSOR C_AUDITORIA IS
        SELECT A.ID_AUDITORIA, A.FECHA_EVENTO, A.ACCION, A.TABLA_AFECTADA, U.NOMBRE_USUARIO
        FROM AUDITORIA A
        INNER JOIN USUARIO_SISTEMA U ON U.ID_USUARIO = A.ID_USUARIO
        ORDER BY A.FECHA_EVENTO DESC;

    V_REG C_AUDITORIA%ROWTYPE;
    V_CONTADOR NUMBER := 0;
BEGIN
    DBMS_OUTPUT.PUT_LINE('REPORTE 3: EVENTOS DE AUDITORIA');

    OPEN C_AUDITORIA;
    FETCH C_AUDITORIA INTO V_REG;

    WHILE C_AUDITORIA%FOUND LOOP
        V_CONTADOR := V_CONTADOR + 1;
        DBMS_OUTPUT.PUT_LINE(V_CONTADOR || '. ' || TO_CHAR(V_REG.FECHA_EVENTO,'YYYY-MM-DD HH24:MI') ||
                             ' - ' || V_REG.ACCION || ' - ' || V_REG.TABLA_AFECTADA ||
                             ' - Usuario: ' || V_REG.NOMBRE_USUARIO);
        FETCH C_AUDITORIA INTO V_REG;
    END LOOP;

    CLOSE C_AUDITORIA;
EXCEPTION
    WHEN OTHERS THEN
        IF C_AUDITORIA%ISOPEN THEN
            CLOSE C_AUDITORIA;
        END IF;
        DBMS_OUTPUT.PUT_LINE('Error en reporte 3: ' || SQLERRM);
END;
/

/* ----------------------------------------------------------------------------
   REPORTE 4: Medicamentos por historia clinica usando RECORD y LOOP.
   Cursor 4.
---------------------------------------------------------------------------- */
DECLARE
    TYPE T_MEDICAMENTO_REC IS RECORD (
        ID_HISTORIA    HISTORIA_MEDICAMENTO.ID_HISTORIA%TYPE,
        ID_MEDICAMENTO MEDICAMENTO.ID_MEDICAMENTO%TYPE,
        NOMBRE         MEDICAMENTO.NOMBRE%TYPE,
        DOSIS          HISTORIA_MEDICAMENTO.DOSIS%TYPE,
        FRECUENCIA     HISTORIA_MEDICAMENTO.FRECUENCIA%TYPE
    );

    CURSOR C_MEDICAMENTOS IS
        SELECT HM.ID_HISTORIA, M.ID_MEDICAMENTO, M.NOMBRE,
               HM.DOSIS, HM.FRECUENCIA
        FROM HISTORIA_MEDICAMENTO HM
        INNER JOIN MEDICAMENTO M ON M.ID_MEDICAMENTO = HM.ID_MEDICAMENTO
        ORDER BY HM.ID_HISTORIA, M.NOMBRE;

    V_MED              T_MEDICAMENTO_REC;
    V_HISTORIA_ANTERIOR NUMBER := -1;
    V_TOTAL            NUMBER := 0;   -- ← variable para guardar conteo antes de cerrar
BEGIN
    DBMS_OUTPUT.PUT_LINE('REPORTE 4: MEDICAMENTOS POR HISTORIA CLINICA');

    OPEN C_MEDICAMENTOS;
    LOOP
        FETCH C_MEDICAMENTOS INTO V_MED;
        EXIT WHEN C_MEDICAMENTOS%NOTFOUND;

        V_TOTAL := V_TOTAL + 1;

        IF V_MED.ID_HISTORIA <> V_HISTORIA_ANTERIOR THEN
            DBMS_OUTPUT.PUT_LINE('-- Historia ID: ' || V_MED.ID_HISTORIA);
            V_HISTORIA_ANTERIOR := V_MED.ID_HISTORIA;
        END IF;

        DBMS_OUTPUT.PUT_LINE('   Medicamento: ' || V_MED.NOMBRE ||
                             ' | Dosis: '       || V_MED.DOSIS  ||
                             ' | Frecuencia: '  || V_MED.FRECUENCIA);
    END LOOP;

    V_TOTAL := C_MEDICAMENTOS%ROWCOUNT;   -- ← guardar ANTES de cerrar
    CLOSE C_MEDICAMENTOS;                 -- ← cerrar despues

    IF V_TOTAL = 0 THEN
        DBMS_OUTPUT.PUT_LINE('No hay medicamentos registrados.');
    END IF;
EXCEPTION
    WHEN OTHERS THEN
        IF C_MEDICAMENTOS%ISOPEN THEN CLOSE C_MEDICAMENTOS; END IF;
        DBMS_OUTPUT.PUT_LINE('Error en reporte 4: ' || SQLERRM);
END;
/

/* ----------------------------------------------------------------------------
   REPORTE 5: Pacientes activos usando TABLE y BULK COLLECT.
   Cumple tipo compuesto TABLE y BULK COLLECT.
---------------------------------------------------------------------------- */
DECLARE
    TYPE T_PACIENTE_TAB IS TABLE OF PACIENTE%ROWTYPE;
    V_PACIENTES T_PACIENTE_TAB;
BEGIN
    DBMS_OUTPUT.PUT_LINE('REPORTE 5: PACIENTES ACTIVOS CARGADOS CON BULK COLLECT');

    SELECT *
    BULK COLLECT INTO V_PACIENTES
    FROM PACIENTE
    WHERE ESTADO = 'A'
    ORDER BY APELLIDOS, NOMBRES;

    IF V_PACIENTES.COUNT = 0 THEN
        DBMS_OUTPUT.PUT_LINE('No hay pacientes activos.');
    ELSE
        FOR I IN 1 .. V_PACIENTES.COUNT LOOP
            DBMS_OUTPUT.PUT_LINE(I || '. ' || V_PACIENTES(I).NOMBRES || ' ' ||
                                 V_PACIENTES(I).APELLIDOS || ' - Doc: ' ||
                                 V_PACIENTES(I).NUM_DOCUMENTO || ' - Email: ' ||
                                 NVL(V_PACIENTES(I).EMAIL, 'SIN EMAIL'));
        END LOOP;
    END IF;
EXCEPTION
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Error en reporte 5: ' || SQLERRM);
END;
