CREATE OR REPLACE PACKAGE BODY PKG_GESTION_CITAS AS

    E_CITA_NO_EXISTE      EXCEPTION;
    E_PACIENTE_NO_EXISTE  EXCEPTION;
    E_MEDICO_NO_EXISTE    EXCEPTION;
    E_ESTADO_INVALIDO     EXCEPTION;
    E_HORA_INVALIDA       EXCEPTION;
    E_FECHA_INVALIDA      EXCEPTION;

    PROCEDURE VALIDAR_ESTADO(
        P_ESTADO IN CITA.ESTADO%TYPE
    ) IS
    BEGIN
        IF UPPER(P_ESTADO) NOT IN ('PROGRAMADA', 'ATENDIDA', 'CANCELADA', 'REPROGRAMADA') THEN
            RAISE E_ESTADO_INVALIDO;
        END IF;
    END VALIDAR_ESTADO;

    PROCEDURE VALIDAR_HORA(
        P_HORA IN CITA.HORA_CITA%TYPE
    ) IS
    BEGIN
        IF NOT REGEXP_LIKE(P_HORA, '^([01][0-9]|2[0-3]):[0-5][0-9]$') THEN
            RAISE E_HORA_INVALIDA;
        END IF;
    END VALIDAR_HORA;

    PROCEDURE VALIDAR_PACIENTE(
        P_ID_PACIENTE IN CITA.ID_PACIENTE%TYPE
    ) IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_TOTAL
        FROM PACIENTE
        WHERE ID_PACIENTE = P_ID_PACIENTE
          AND ESTADO = 'A';

        IF V_TOTAL = 0 THEN
            RAISE E_PACIENTE_NO_EXISTE;
        END IF;
    END VALIDAR_PACIENTE;

    PROCEDURE VALIDAR_MEDICO(
        P_ID_MEDICO IN CITA.ID_MEDICO%TYPE
    ) IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_TOTAL
        FROM MEDICO_TERAPISTA
        WHERE ID_MEDICO = P_ID_MEDICO
          AND ESTADO = 'A';

        IF V_TOTAL = 0 THEN
            RAISE E_MEDICO_NO_EXISTE;
        END IF;
    END VALIDAR_MEDICO;

    PROCEDURE INSERTAR_CITA(
        P_ID_PACIENTE   IN CITA.ID_PACIENTE%TYPE,
        P_ID_MEDICO     IN CITA.ID_MEDICO%TYPE,
        P_FECHA_CITA    IN CITA.FECHA_CITA%TYPE,
        P_HORA_CITA     IN CITA.HORA_CITA%TYPE,
        P_MOTIVO        IN CITA.MOTIVO%TYPE,
        P_OBSERVACIONES IN CITA.OBSERVACIONES%TYPE,
        P_ID_GENERADO   OUT CITA.ID_CITA%TYPE
    ) IS
    BEGIN
        IF P_FECHA_CITA < TRUNC(SYSDATE) THEN
            RAISE E_FECHA_INVALIDA;
        END IF;

        VALIDAR_HORA(P_HORA_CITA);
        VALIDAR_PACIENTE(P_ID_PACIENTE);
        VALIDAR_MEDICO(P_ID_MEDICO);

        P_ID_GENERADO := SEQ_CITA.NEXTVAL;

        INSERT INTO CITA(
            ID_CITA,
            ID_PACIENTE,
            ID_MEDICO,
            FECHA_CITA,
            HORA_CITA,
            ESTADO,
            MOTIVO,
            OBSERVACIONES,
            FECHA_REGISTRO
        ) VALUES (
            P_ID_GENERADO,
            P_ID_PACIENTE,
            P_ID_MEDICO,
            P_FECHA_CITA,
            P_HORA_CITA,
            'PROGRAMADA',
            INITCAP(P_MOTIVO),
            P_OBSERVACIONES,
            SYSDATE
        );

        DBMS_OUTPUT.PUT_LINE('Cita insertada correctamente con ID: ' || P_ID_GENERADO);

    EXCEPTION
        WHEN E_FECHA_INVALIDA THEN
            RAISE_APPLICATION_ERROR(-20201, 'No se puede crear una cita en una fecha pasada.');
        WHEN E_HORA_INVALIDA THEN
            RAISE_APPLICATION_ERROR(-20202, 'La hora debe tener formato HH24:MI. Ejemplo: 08:30.');
        WHEN E_PACIENTE_NO_EXISTE THEN
            RAISE_APPLICATION_ERROR(-20203, 'No existe un paciente activo con el ID indicado.');
        WHEN E_MEDICO_NO_EXISTE THEN
            RAISE_APPLICATION_ERROR(-20204, 'No existe un medico/terapista activo con el ID indicado.');
        WHEN DUP_VAL_ON_INDEX THEN
            RAISE_APPLICATION_ERROR(-20205, 'Ya existe una cita para ese medico en la misma fecha y hora.');
        WHEN VALUE_ERROR THEN
            RAISE_APPLICATION_ERROR(-20206, 'Error de tipo o longitud en los datos de la cita.');
        WHEN OTHERS THEN
            RAISE_APPLICATION_ERROR(-20207, 'Error inesperado al insertar cita: ' || SQLERRM);
    END INSERTAR_CITA;

    PROCEDURE ACTUALIZAR_CITA(
        P_ID_CITA       IN CITA.ID_CITA%TYPE,
        P_ID_MEDICO     IN CITA.ID_MEDICO%TYPE,
        P_FECHA_CITA    IN CITA.FECHA_CITA%TYPE,
        P_HORA_CITA     IN CITA.HORA_CITA%TYPE,
        P_ESTADO        IN CITA.ESTADO%TYPE,
        P_MOTIVO        IN CITA.MOTIVO%TYPE,
        P_OBSERVACIONES IN CITA.OBSERVACIONES%TYPE
    ) IS
        V_TOTAL NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_TOTAL
        FROM CITA
        WHERE ID_CITA = P_ID_CITA;

        IF V_TOTAL = 0 THEN
            RAISE E_CITA_NO_EXISTE;
        END IF;

        IF P_FECHA_CITA < TRUNC(SYSDATE) AND UPPER(P_ESTADO) IN ('PROGRAMADA', 'REPROGRAMADA') THEN
            RAISE E_FECHA_INVALIDA;
        END IF;

        VALIDAR_HORA(P_HORA_CITA);
        VALIDAR_ESTADO(P_ESTADO);
        VALIDAR_MEDICO(P_ID_MEDICO);

        UPDATE CITA
        SET ID_MEDICO = P_ID_MEDICO,
            FECHA_CITA = P_FECHA_CITA,
            HORA_CITA = P_HORA_CITA,
            ESTADO = UPPER(P_ESTADO),
            MOTIVO = INITCAP(P_MOTIVO),
            OBSERVACIONES = P_OBSERVACIONES
        WHERE ID_CITA = P_ID_CITA;

        DBMS_OUTPUT.PUT_LINE('Cita actualizada correctamente: ' || P_ID_CITA);

    EXCEPTION
        WHEN E_CITA_NO_EXISTE THEN
            RAISE_APPLICATION_ERROR(-20208, 'No existe la cita que se desea actualizar.');
        WHEN E_FECHA_INVALIDA THEN
            RAISE_APPLICATION_ERROR(-20209, 'No se puede programar o reprogramar una cita en una fecha pasada.');
        WHEN E_HORA_INVALIDA THEN
            RAISE_APPLICATION_ERROR(-20210, 'La hora actualizada debe tener formato HH24:MI.');
        WHEN E_ESTADO_INVALIDO THEN
            RAISE_APPLICATION_ERROR(-20211, 'Estado invalido para la cita.');
        WHEN E_MEDICO_NO_EXISTE THEN
            RAISE_APPLICATION_ERROR(-20212, 'No existe un medico/terapista activo con el ID indicado.');
        WHEN DUP_VAL_ON_INDEX THEN
