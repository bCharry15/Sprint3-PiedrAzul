/* ============================================================================
   g) TRIGGERS - REGLAS DE NEGOCIO RELACIONADAS CON RESTRICCIONES DE DATOS
   k) Regla de negocio de formato: HORA_CITA HH24:MI y correo valido.
============================================================================ */

/* Trigger 1: valida fecha y formato de hora de la cita. */
CREATE OR REPLACE TRIGGER TRG_CITA_VALIDAR_FECHA_HORA
BEFORE INSERT OR UPDATE OF FECHA_CITA, HORA_CITA, ESTADO ON CITA
FOR EACH ROW
BEGIN
    IF :NEW.FECHA_CITA < TRUNC(SYSDATE) AND :NEW.ESTADO IN ('PROGRAMADA','REPROGRAMADA') THEN
        RAISE_APPLICATION_ERROR(-20101, 'No se puede programar o reprogramar una cita en una fecha pasada.');
    END IF;

    IF NOT REGEXP_LIKE(:NEW.HORA_CITA, '^([01][0-9]|2[0-3]):[0-5][0-9]$') THEN
        RAISE_APPLICATION_ERROR(-20102, 'La hora de la cita debe tener formato HH24:MI, ejemplo 08:30.');
    END IF;
END;
/

/* Trigger 2: valida formato de datos del paciente y normaliza nombres/correo. */
CREATE OR REPLACE TRIGGER TRG_PACIENTE_VALIDAR_FORMATOS
BEFORE INSERT OR UPDATE ON PACIENTE
FOR EACH ROW
BEGIN
    :NEW.TIPO_DOCUMENTO := UPPER(:NEW.TIPO_DOCUMENTO);
    :NEW.NOMBRES := INITCAP(:NEW.NOMBRES);
    :NEW.APELLIDOS := INITCAP(:NEW.APELLIDOS);

    IF :NEW.FECHA_NACIMIENTO > TRUNC(SYSDATE) THEN
        RAISE_APPLICATION_ERROR(-20103, 'La fecha de nacimiento no puede ser futura.');
    END IF;

    IF :NEW.EMAIL IS NOT NULL THEN
        :NEW.EMAIL := LOWER(:NEW.EMAIL);
        IF NOT REGEXP_LIKE(:NEW.EMAIL, '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$') THEN
            RAISE_APPLICATION_ERROR(-20104, 'El email del paciente no tiene un formato valido.');
        END IF;
    END IF;

    IF :NEW.CELULAR IS NOT NULL AND NOT REGEXP_LIKE(:NEW.CELULAR, '^[0-9]{10}$') THEN
        RAISE_APPLICATION_ERROR(-20105, 'El celular debe contener exactamente 10 digitos numericos.');
    END IF;
END;
/

/* Trigger 3: valida intervalos permitidos del medico/terapista. */
CREATE OR REPLACE TRIGGER TRG_MEDICO_VALIDAR_INTERVALO
BEFORE INSERT OR UPDATE OF INTERVALO_MINUTOS, EMAIL ON MEDICO_TERAPISTA
FOR EACH ROW
BEGIN
    CASE :NEW.INTERVALO_MINUTOS
        WHEN 15 THEN NULL;
        WHEN 30 THEN NULL;
        WHEN 45 THEN NULL;
        WHEN 60 THEN NULL;
        ELSE RAISE_APPLICATION_ERROR(-20106, 'El intervalo del profesional solo puede ser 15, 30, 45 o 60 minutos.');
    END CASE;

    IF :NEW.EMAIL IS NOT NULL THEN
        :NEW.EMAIL := LOWER(:NEW.EMAIL);
        IF NOT REGEXP_LIKE(:NEW.EMAIL, '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$') THEN
            RAISE_APPLICATION_ERROR(-20107, 'El email del medico/terapista no tiene formato valido.');
        END IF;
    END IF;
END;
/

/* Trigger 4: solo permite historia clinica si la cita esta atendida. */
CREATE OR REPLACE TRIGGER TRG_HISTORIA_SOLO_CITA_ATENDIDA
BEFORE INSERT OR UPDATE OF ID_CITA ON HISTORIA_CLINICA
FOR EACH ROW
DECLARE
    V_ESTADO CITA.ESTADO%TYPE;
BEGIN
    SELECT ESTADO INTO V_ESTADO
    FROM CITA
    WHERE ID_CITA = :NEW.ID_CITA;

    IF V_ESTADO <> 'ATENDIDA' THEN
        RAISE_APPLICATION_ERROR(-20108, 'Solo se puede crear historia clinica para citas en estado ATENDIDA.');
    END IF;

EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20109, 'La cita asociada a la historia clinica no existe.');
END;
/

/* Trigger 5 adicional: auditoria automatica de cambios sobre CITA. */
CREATE OR REPLACE TRIGGER TRG_CITA_AUDITORIA
AFTER INSERT OR UPDATE OR DELETE ON CITA
FOR EACH ROW
DECLARE
    V_ACCION AUDITORIA.ACCION%TYPE;
    V_DESC   AUDITORIA.DESCRIPCION%TYPE;
BEGIN
    IF INSERTING THEN
        V_ACCION := 'INSERTAR_CITA';
        V_DESC := 'Se inserto la cita ' || :NEW.ID_CITA || ' para el paciente ' || :NEW.ID_PACIENTE;
    ELSIF UPDATING THEN
        V_ACCION := 'ACTUALIZAR_CITA';
        V_DESC := 'La cita ' || :NEW.ID_CITA || ' cambio de estado ' || :OLD.ESTADO || ' a ' || :NEW.ESTADO;
    ELSE
        V_ACCION := 'ELIMINAR_CITA';
        V_DESC := 'Se elimino la cita ' || :OLD.ID_CITA;
    END IF;

    INSERT INTO AUDITORIA(ID_AUDITORIA, ID_USUARIO, FECHA_EVENTO, ACCION, TABLA_AFECTADA, DESCRIPCION)
    VALUES(SEQ_AUDITORIA.NEXTVAL, 1, SYSDATE, V_ACCION, 'CITA', V_DESC);
END;
/

