   h) CREACION Y GENERACION DE VISTA - 1 VISTA
============================================================================ */
CREATE OR REPLACE VIEW VW_RESUMEN_CITAS_PROFESIONAL AS
SELECT
    M.ID_MEDICO,
    M.NOMBRES || ' ' || M.APELLIDOS AS PROFESIONAL,
    M.TIPO_PROFESIONAL,
    COUNT(C.ID_CITA) AS TOTAL_CITAS,
    SUM(CASE WHEN C.ESTADO = 'PROGRAMADA' THEN 1 ELSE 0 END) AS CITAS_PROGRAMADAS,
    SUM(CASE WHEN C.ESTADO = 'ATENDIDA' THEN 1 ELSE 0 END) AS CITAS_ATENDIDAS,
    SUM(CASE WHEN C.ESTADO = 'CANCELADA' THEN 1 ELSE 0 END) AS CITAS_CANCELADAS,
    SUM(CASE WHEN C.ESTADO = 'REPROGRAMADA' THEN 1 ELSE 0 END) AS CITAS_REPROGRAMADAS
FROM MEDICO_TERAPISTA M
LEFT JOIN CITA C ON C.ID_MEDICO = M.ID_MEDICO
GROUP BY M.ID_MEDICO, M.NOMBRES, M.APELLIDOS, M.TIPO_PROFESIONAL;
/

